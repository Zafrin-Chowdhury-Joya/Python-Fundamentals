# Define a list of programming languages (string list)
programming = ["Python", "Java", "C#", "C++", "Javascript"]

# Sort the list in ascending (alphabetical) order
programming.sort()
print("Ascending order (strings):", programming)

# Sort the list in descending order
programming.sort(reverse=True)
print("Descending order (strings):", programming)

# -----------------------------------
# Define a list of numbers
programming = [2, 3, 6, 10, 2, 6, 9, 0, 8, 33]

# Sort numbers in ascending order (small → large)
programming.sort()
print("Ascending order (numbers):", programming)

# Sort numbers in descending order (large → small)
programming.sort(reverse=True)
print("Descending order (numbers):", programming)