# Declare a list
programming = ["Python", "Java", "C#","C++","Javascript"]
# Print the list
print(programming)
# Access a specific item
print(programming[2])
# Negative Index
print(programming[-3])
# Range of Index[start, end-1]
print(programming[2:4])
# Check if Item exist
if "Python" in programming:
    print("Python is awesome")

