# Convert int to float
a = 10
b = float(a)
print("Int to Float:", b, "Type:", type(b))

# Convert float to int
c = 5.8
d = int(c)
print("Float to Int:", d, "Type:", type(d))

# Convert int to string
e = 100
f = str(e)
print("Int to String:", f, "Type:", type(f))

# Convert string to int
g = "50"
h = int(g)
print("String to Int:", h, "Type:", type(h))