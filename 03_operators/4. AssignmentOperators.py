test_count = 5

test_count += 2   # test_count = test_count + 2
print("After += :", test_count)

test_count -= 1   # test_count = test_count - 1
print("After -= :", test_count)

test_count *= 2   # test_count = test_count * 2
print("After *= :", test_count)

test_count /= 2   # test_count = test_count / 2
print("After /= :", test_count)